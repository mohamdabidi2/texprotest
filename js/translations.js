// Translation function
function translatePage(lang) {
    // First, translate nested elements (span, strong with data-i18n)
    const nestedElements = document.querySelectorAll('span[data-i18n], strong[data-i18n]');
    nestedElements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Then translate all other elements
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        // Skip if already processed as nested element
        if (element.tagName === 'SPAN' || element.tagName === 'STRONG') {
            return;
        }
        
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translations[lang][key];
            } else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'BUTTON') {
                element.textContent = translations[lang][key];
            } else {
                // Check if element has nested elements with data-i18n
                const nestedI18n = element.querySelectorAll('span[data-i18n], strong[data-i18n]');
                if (nestedI18n.length > 0) {
                    // For elements with nested translations, preserve HTML structure
                    // Only replace text nodes that are direct children
                    const directTextNodes = Array.from(element.childNodes).filter(node => 
                        node.nodeType === 3 && node.textContent.trim()
                    );
                    // Replace the first direct text node if it exists
                    if (directTextNodes.length > 0) {
                        directTextNodes[0].textContent = translations[lang][key];
                    }
                } else {
                    // For simple elements, replace all text content
                    element.textContent = translations[lang][key];
                }
            }
        }
    });
    
    // Update language switcher active state
    document.querySelectorAll('.lang-link').forEach(link => {
        if (link.getAttribute('data-lang') === lang) {
            link.classList.add('active');
        } else {
            link.classList.remove('active');
        }
    });
    
    // Save language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Initialize translation on page load
document.addEventListener('DOMContentLoaded', function() {
    // Use saved language if available, otherwise default to English
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    translatePage(savedLang);
    
    // Language switcher click handlers
    document.querySelectorAll('.lang-link').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const selectedLang = this.getAttribute('data-lang');
            translatePage(selectedLang);
        });
    });
});
